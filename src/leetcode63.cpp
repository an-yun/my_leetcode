class Solution
{
  public:
    int uniquePathsWithObstacles(vector<vector<int>> &obstacleGrid)
    {
        int m = obstacleGrid.size();
        int n = obstacleGrid[0].size();
        if (obstacleGrid[0][0] == 1)
            return 0;
        vector<unsigned> board(n, 1);
        for (int j = 1; j < n; j++)
        {
            board[j] = obstacleGrid[0][j] ? 0 : board[j-1];
        }
        for (int i = 1; i < m; i++)
        {
            board[0] = obstacleGrid[i][0] ? 0 : board[0];
            for (int j = 1; j < n; j++)
            {
                 board[j] = obstacleGrid[i][j] ? 0 : board[j-1]+board[j];
            }
        }
        return board[n - 1];
    }
};

